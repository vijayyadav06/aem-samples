package com.medtronic.com.app;

import org.apache.commons.lang.StringUtils;

/**
 * This is a FooterObject Entity Java file that should be used for Footer.
 * @author sunny.sima
 *
 */
public class FooterLinkObject {
    private String text;
    private String url;
    private boolean newTab;
    private String fontWeight;
    
    /**
     * Initialize the fields.
     * 
     * @param text - text
     * @param url - url
     * @param newTab - new tab
     */
    public FooterLinkObject(final String text, final String url, final boolean newTab) {
        this(text, url, newTab, StringUtils.EMPTY);
    }

    /**
     * Initialize the fields.
     * 
     * @param text - text
     * @param url - url
     * @param newTab - new tab
     * @param fontWeight - font weight
     */
    public FooterLinkObject(final String text, final String url, final boolean newTab, final String fontWeight) {
        this.text = text;
        this.url = url;
        this.newTab = newTab;
        this.fontWeight = fontWeight;
    }

    /**Returns the text.
     * 
     * @return the text
     */
    public String getText() {
        return text;
    }
    
    /**
     * Sets the text.
     * 
     * @param text the text to set
     */
    public void setText(final String text) {
        this.text = text;
    }
    
    /**
     * Returns the url.
     * 
     * @return the url
     */
    public String getUrl() {
        return url;
    }
    
    /**
     * Sets the url.
     * 
     * @param url the url to set
     */
    public void setUrl(final String url) {
        this.url = url;
    }
    
    /**
     * Returns the newTab.
     * 
     * @return the newTab
     */
    public boolean isNewTab() {
        return newTab;
    }
    
    /**
     * Sets the newTab.
     * 
     * @param newTab the newTab to set
     */
    public void setNewTab(final boolean newTab) {
        this.newTab = newTab;
    }
    
    /**
     * Returns the font weight.
     * 
     * @return the fontWeight
     */
    public String getFontWeight() {
        return fontWeight;
    }
    
    /**
     * Sets the font weight.
     * 
     * @param fontWeight the fontWeight to set
     */
    public void setFontWeight(final String fontWeight) {
        this.fontWeight = fontWeight;
    }
    
}
