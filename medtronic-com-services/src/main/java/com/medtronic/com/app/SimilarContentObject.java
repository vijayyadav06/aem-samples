
package com.medtronic.com.app;

/**
 * This is SimilarContent Object
 * 
 * @author fredy.xue
 */
public class SimilarContentObject {

    private String image;
    private String text;
    private String name;
    private String link;

    public SimilarContentObject() {

    }

    public SimilarContentObject(String image, String text, String name, String link) {
        this.image = image;
        this.text = text;
        this.name = name;
        this.link = link;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}
